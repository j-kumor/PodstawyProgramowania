# Calculation of circle area and circumference 

# Determine radius and PI values
PI = 3.14
r = float(input("Enter the radius: "))

# Calculate area
area = PI * r**2

# Calculate circumference (obwod)
circumference = 2 * PI * r

# Print results
print(f"Radius: {r}")
print(f"Area: {area}")
print(f"Circumference: {circumference}")